# 리스트 초기화 대괄호 안에 원소를 넣어 초기화, 쉼표로 원소 구분
# 비어 있는 리스트 선언 시에는 [] 또는 list()


# 크기가 n이고, 모든 값이 0인 1차원 리스트 초기화
a = []
n =10
a = [0]*n

