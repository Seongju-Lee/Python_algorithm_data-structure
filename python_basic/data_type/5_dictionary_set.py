# 집합 자료형
# set() 함수 이용
# 리스트 혹은 문자열을 이용해서 초기화 가능
# 또는, 중괄호 안에 각 원소를 ,로 구분하여 삽입함으로써 초기화 가능.
# 데이터의 조회 및 수정에 있어서 상수시간(O(1))을 처리할 수 있다는 점에서 사전 자료형과 동일
# 중복 허용 x, 순서가 없다

# 방법1. 리스트를 이용한 초기화
data = set([1,2,3,4,4,5,5,6]) # 리스트를 이용 => 중복은 제거됨
print(data)

# 방법2. {}내부에서 콤마를 기준으로 초기화
data = {1,2,3,4,5,6,6} 
print(data)

for i in data:
    print(i)
# 집합 자료형은 합집합, 교집합, 차집합 연산 등이 가능하다.
# |: 합집합, &: 교집합, -: 차집합

# 새로운 원소 추가
data.add(7)
print(data)
# 새로운 원소 여러개 추가
data.update([8,9])
print(data)
# 특정한 값을 갖는 원소 제거
data.remove(1)
print(data)